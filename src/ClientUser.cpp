#include "ClientUser.h"

#include "Client.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

tpmclient::ClientUser::ClientUser(const Client& client)
: mClient{client}
{
    mClient.attemptToUse();
    mClient.flushKeys();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

tpmclient::ClientUser::~ClientUser()
{
    mClient.flushKeys();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const tpmclient::Client& tpmclient::ClientUser::getClient() const
{
    return mClient;
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#ifndef TPM_CLIENT_CLIENT_USER_H
#define TPM_CLIENT_CLIENT_USER_H

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

namespace tpmclient
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Client;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/**
 * Wrapper for a `Client` object that sets it up in a RAII style.
 */
class ClientUser
{
public:
    explicit ClientUser(const Client& client);

    ClientUser(const ClientUser&) = delete;
    ClientUser& operator=(const ClientUser&) = delete;
    ClientUser(ClientUser&&) = delete;
    ClientUser& operator=(ClientUser&&) = delete;

    ~ClientUser();

    /**
     * Returns the `Client` object.
     */
    const Client& getClient() const;

private:
    const Client& mClient;
};

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#endif

